    private void btnSalvarActionPerformed(java.awt.event.ActionEvent evt) {//GEN-FIRST:event_btnSalvarActionPerformed
        DateTimeFormatter formatter = DateTimeFormatter.ofPattern("dd-MM-yyyy");
        try{
            this.vendedor.setNome(txtNome.getText());
            this.vendedor.setCpf(txtCpf.getText());
            //sintaxe de convers√£o
            this.vendedor.setDataNascimento(LocalDate.parse(txtDataNascimento.getText(),formatter));
            this.vendedor.setTelefone(txtTelefone.getText());
            this.vendedor.setSalario(Double.parseDouble(txtSalario.getText()));
            this.vendedor.setComissao(Double.parseDouble(txtComissao.getText()));
        
        this.dispose();
        }catch(DateTimeParseException e1){
            JOptionPane.showMessageDialog(rootPane, "Data inv√°lida!! Informe data no formato dd-MM-yyyy\n"+e1);
        }catch (NumberFormatException e2){
            JOptionPane.showMessageDialog(rootPane, "Valores inv√°lidos!! Em sal√°rio e comiss√£o informe somente valores num√©ricos\n"+e2);
        }catch (Exception e3) {
            JOptionPane.showMessageDialog(rootPane, "Ocorreu um erro inesperado: \n"+e3);
        } 
    }//GEN-LAST:event_btnSalvarActionPerformed

